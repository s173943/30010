void blinkSelect(int8_t maxWindows, int8_t * selector){

    if((readJoystick()>>3)&1){
        *selector = *selector + 1;
        if (*selector >= maxWindows+1){
            *selector = 1;
        }
    }
    else if((readJoystick()>>2)&1){
        *selector = *selector - 1;
        if (*selector <= 0){
            *selector = maxWindows;
        }
    }
    if(maxWindows == 4){
        if((readJoystick() >> 0) & 1){
            if (*selector+2 == 5){
                *selector = 1;
            }
            else if(*selector+2 == 6){
                *selector = 2;
            }
            else{
                *selector = *selector + 2;
            }
        }
        if((readJoystick() >> 1) & 1){
            if (*selector-2 == -1){
                *selector = 3;
            }
            else if(*selector-2 == 0){
                *selector = 4;
            }
            else{
                *selector = *selector -2;
            }
        }
    }
}


